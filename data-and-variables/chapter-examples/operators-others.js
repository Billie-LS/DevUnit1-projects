/*
OTHER OPERATORS
The String Operator +
the + operator can be used with string operands to concatenate, 
or join together two strings
i.e. "Launch" + "Code" evaluates to "LaunchCode"
*/
console.log(1 + 1);
console.log(typeof(1 + 1));

console.log("1" + "1");
console.log(typeof("1" + "1"));

// Compound Assignment Operators
let x = 1;
x = x + 1;

console.log(x);

let y = 1;
y += 1;

console.log(y);